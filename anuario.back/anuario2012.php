                   <p align="justify"><strong>Abstract:</strong> Notwithstanding the existence of a conventional instrument in the matter, the law of the sea in general and with regard to the exploitation of minerals in the so-called Area, is still in a phase of implementation and precise definition of the rights and obligations of the States. One example that has become apparent is with respect to the obligations of States that sponsor individuals to lead the exploration and exploitation of the Area. This issue was submitted in 2010, by the International Seabed Authority to the International Tribunal for the Law of the Sea so it could be clarified. The advisory opinion of the Tribunal advanced conclusions particularly relevant for the determination of a nationality link between the legal persons and the States that sponsor them. This matter, which plays an essential role for achieving an effective protection of the marine environment within the sponsorship mechanisms for exploration and exploitation of the resources of the Area, also has some relevant consequences for the activities in reserved areas.</p>
                   <p align="justify"><strong>Keywords:</strong> unclos, International Seabed Authority, International Tribunal
for the Law of the Sea, Activities in the Area, nationality of legal persons,
effective control.</p>
                   <p align="justify"><strong>Resumen:</strong> No obstante la existencia de un instrumento convencional en la materia, el derecho del mar en general, y lo relativo a la explotaci&oacute;n de minerales en la Zona, se encuentra a&uacute;n en una fase de puesta en pr&aacute;ctica y de definici&oacute;n fina de los derechos y obligaciones de los Estados. Uno de los ejemplos que se ha hecho patente es lo relativo a las obligaciones de aquellos Estados que patrocinan a particulares a fin de llevar actividades de exploraci&oacute;n y explotaci&oacute;n de los recursos de la Zona. Esta cuesti&oacute;n fue remitida en 2010 por la Autoridad Internacional de Fondos Marinos al Tribunal Internacional de Derecho del Mar a fin de que este la dilucidara. La Opini&oacute;n Consultiva que emiti&oacute; el Tribunal presenta conclusiones de particular relevancia en materia de la determinaci&oacute;n de un v&iacute;nculo efectivo entre las personas jur&iacute;dicas y los Estados que las patrocinan. Esta cuesti&oacute;n, que juega un papel determinante a fin de lograr una eficaz protecci&oacute;n del medio marino en el marco del mecanismo de patrocinio para la exploraci&oacute;n y explotaci&oacute;n de los recursos en la Zona, conlleva importantes consecuencias para las actividades en &aacute;reas reservadas.</p>
                   <p align="justify"><strong>Palabras  clave:</strong> convemar, Autoridad Internacional de Fondos Marinos, Tribunal
Internacional de Derecho del Mar, actividades en la Zona, nacionalidad personas jur&iacute;dicas, control efectivo.</p>
