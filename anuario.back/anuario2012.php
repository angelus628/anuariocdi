              <em>Rosmerlin Estupi&ntilde;&aacute;n Silva (Espa&ntilde;a-Colombia)</em></p>
 <a id="displayText2" href="javascript:toggle2();">Resumen</a><a href="anuario2012/ACDI Vol 5 Les mesures.pdf" target="_blank">/ Archivo Completo </a>
