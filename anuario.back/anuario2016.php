          <p align="justify"><strong>Abstract: </strong> In the Inter-American context, the control of conventionality promoted by the Inter-American Court of Human Rights is linked to the process of construction of an <em>ius commune</em> in human rights. Human rights are identified as norm of constitutional character. The universality of human rights allows its consideration as an aspect of global constitutionalism. This paper aims at determining whether the control of conventionalit can be considered an expression of global constitutionalism within the Inter-American region. We hold that the control of conventionality in the Inter-American system has propelled the application of a minimum standard of human rights and has stimulated the emergence of an <em>ius commune</em> in human rights.</p>
            <p align="justify"><strong>Keywords: </strong> Inter-american Comission on Human Rights, International Human Rights Law, Control of Conventionality, <em>ius commune interamericanum</em>, Global Constitutionalism </p>
