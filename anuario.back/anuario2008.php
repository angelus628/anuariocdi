          <td height="73" valign="top"><p><strong>The mutual agreement procedure and arbitration of double taxation disputes </strong><br />
            Ilias Bantekas (Reino Unido)</p>
            
            <a id="displayText7" href="javascript:toggle7();">Resumen</a><a href="anuario2008/9_Bantekas.pdf" target="_blank">/ Archivo Completo </a>
        <br>&nbsp;</br>
        <div id="toggleText7" style="display: none">
            
            <p align="justify"><strong>Resumen:</strong> Hay un claro deseo de los Estados de eliminar la doble tributaci&oacute;n,  el pago de un mismo impuesto en dos jurisdicciones diferentes, de las empresas  multinacionales. Lo anterior en la medida que, por un lado, la imposici&oacute;n  tributaria es un derecho universalmente aceptado del Estado, por el otro, los  diferendos en materia tributaria no son f&aacute;cilmente susceptibles de arbitraje.  Este art&iacute;culo analiza dos procedimientos de soluci&oacute;n de controversias, el de la  OECD, primer modelo de tratado sobre impuestos, y un convenio similar de la UE  espec&iacute;fico en soluci&oacute;n de controversias relativas a la doble imposici&oacute;n.  Analiza los diferentes mecanismos que el Estado puede asumir durante el  procedimiento, marcando las diferencias con la protecci&oacute;n diplom&aacute;tica en favor  de la empresa afectada. El art&iacute;culo examina las situaciones bajo las cuales se  necesita y/o existe un proceso de soluci&oacute;n de controversias; como se inician  los procedimientos, cuales las obligaciones y las partes, los medios como se  resuelven los conflictos, desde la negociaci&oacute;n hasta acudir a un tribunal y las  limitaciones de los procedimientos. En conclusi&oacute;n, el lector podr&aacute; analizar y  comparar los dos procesos. Finalmente, el art&iacute;culo nos muestra la cl&aacute;usula de arbitraje  que se pretende incluir en el art&iacute;culo 25 del modelo de la Convenci&oacute;n sobre  impuestos as&iacute; como si estos mecanismos son incompatibles con los tratados  bilaterales en materia de impuestos y con los GATS. </p>
            <p align="justify"><strong>Palabras  clave:</strong> Doble tributaci&oacute;n, arbitraje, Convenio  Modelo sobre impuestos de la OECD, soluci&oacute;n de controversias, procedimientos de  mutuo acuerdo, expropiaci&oacute;n incrementada, tratados bilaterales de impuestos,  GATS</p>
            <p align="justify"><strong>Abstract:</strong> It is in the interest of most states to eliminate  double taxation (i.e. the payment of the same tax in two jurisdictions) of  transnational commercial enterprises. Because such disputes involve, on the one  hand, the state imposition of taxes, a right universally asserted by all  states, and private entities on the other, taxation disputes between such  parties are not, on their face, easily susceptible to arbitration. This article  analyzes two dispute settlement procedures-the OECD First Model Tax Convention  and a similar EU Convention-with the exclusive focus on disputes relating to  the imposition of double taxation. It will look at the ways in which state  roles may vary under these procedures from assisting in the negotiation process  to taking a part similar to, but with important differences from, diplomatic  protection on behalf of an affected enterprise. The article will examine the  situations under which the settlement procedure is required and/or available,  how the procedures are triggered, the obligations and parts played by the  parties, the means by which the disputes are resolved (from negotiations to  tribunals) and the limitations of the procedures. Are they &ldquo;taxpayer friendly&rdquo;?  As a result the reader may draw comparisons between the two procedures.  Finally, the article will look at the proposed OECD Arbitration Clause which is  intended to be incorporated into Article 25 of the OECD Model Tax Convention as  well as how these mechanisms relate and/or conflict with bilateral tax treaties  and the GATS.</p>
            <strong>Key words:</strong> Double taxation, arbitration, OECD Model Tax  Convention, dispute settlement, Mutual Agreement Procedure, creeping  expropriation, bilateral tax treaties, GATS.<br />
