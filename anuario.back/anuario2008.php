          <td height="87" valign="top"><p><strong>Alternativas de reparaci&oacute;n en el arbitraje internacional de inversiones - Un debate entre la teor&iacute;a y <br />
            la pr&aacute;ctica</strong><br />            
            <em>Nigel Blackaby y Andrea Camargo (Reino Unido y Colombia) </em></p>
            
             <a id="displayText6" href="javascript:toggle6();">Resumen</a><a href="anuario2008/8_Blackaby-Camargo.pdf" target="_blank">/ Archivo Completo </a>
        <br>&nbsp;</br>
        <div id="toggleText6" style="display: none">
            <p align="justify"><strong>Resumen:</strong> El cuestionamiento sobre las alternativas de reparaci&oacute;n en  conflictos derivados de inversiones internacionales resulta de gran inter&eacute;s,  toda vez que no siempre un laudo pecuniario resulta ser el remedio apropiado  para resolver conflictos entre inversionistas y Estados. Esto es aun m&aacute;s  evidente si tenemos en cuenta que los Estados parecen estar cada vez m&aacute;s  interesados en optar por diferentes tipos de reparaci&oacute;n. Adem&aacute;s de ello, no  resulta ser del todo preciso afirmar que exista una postura definitiva y  reiterada por medio de la cual se pueda verificar que los tribunales arbitrales  reconozcan alternativas de reparaci&oacute;n. </p>
            <p align="justify">Este cuestionamiento tiene dos componentes:  el primero es identificar si los tribunales tienen la posibilidad de emitir  laudos &ldquo;no pecuniarios&rdquo;; y el segundo consiste en la posibilidad de ejecutar  laudos de naturaleza &ldquo;no pecuniaria&rdquo;.</p>
            <p align="justify">En este art&iacute;culo se abordan los dos  componentes, siempre girando en torno a una preocupaci&oacute;n central; el respeto a  la soberan&iacute;a, en el entendido de que es un atributo inherente a la soberan&iacute;a de  cada Estado aceptar o no la ejecuci&oacute;n de una decisi&oacute;n de un tribunal arbitral  que lo obligue a cesar o realizar un acto en su territorio.</p>
            <p align="justify"><strong>Palabras  clave:</strong> Alternativas de reparaci&oacute;n en arbitraje  internacional de inversiones.</p>
            <p align="justify"><strong>Summary:</strong> An analysis of the alternatives of  compensation in relation to international investment disputes is relevant,  because a pecuniary award is not always the appropriate remedy to solve  disputes arising between investors and States. This is the case because States  may be increasingly interested in opting for a different type of compensation.  Furthermore, it is still not clear whether arbitral tribunals have recognised  alternative types of awarding damages in respect of international investments  disputes.</p>
            <p align="justify">This  analysis comprises two principal components, the first, is to identify whether  or not the tribunals may render an award that not only demands the payment of a  sum of money but also considers some other means of compensation. The second,  centres on how compliance with these non-pecuniary awards may be demanded.</p>
            <p align="justify">Our  approach to these two principal components will always revolve around the idea  of respecting the sovereignty of the State, bearing in mind that the execution  of an arbitral award, which obliges the State to refrain from or to perform an  act in its territory, relies precisely on the sovereignty of the State to  execute it.</p>
            <strong>Key words:</strong> Non pecuniary remedies in international  investment arbitration.<br />
            <p></p></div></td>
