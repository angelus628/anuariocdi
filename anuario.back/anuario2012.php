        <p align="justify"><strong>Resumen:</strong> La regulaci&oacute;n de los flujos migratorios en Europa produce una tensi&oacute;n constante entre el ejercicio soberano del control fronterizo y el deber estatal de respeto a los derechos humanos. En este sentido, la Corte Europea de Derechos Humanos ha desarrollado una jurisprudencia que trata de restablecer el equilibrio entre el respeto a la vida privada y familiar de los extranjeros y la legitimidad de las medidas de alejamiento dictadas por los Estados. Este art&iacute;culo de reflexi&oacute;n estudia la aplicaci&oacute;n del art&iacute;culo 8 de la Convenci&oacute;n Europea de los Derechos Humanos: &ldquo;Derecho al respecto de la vida privada y familiar&rdquo;, teniendo en cuenta el llamado margen nacional de apreciaci&oacute;n de los Estados y el an&aacute;lisis de la proporcionalidad, en la jurisprudencia de la Corte. Acto seguido, esta reflexi&oacute;n muestra la evoluci&oacute;n jurisprudencial en la materia, para finalizar con un balance entre la efectividad del art&iacute;culo 8 de la Convenci&oacute;n, habida cuenta del endurecimiento de las condiciones y garant&iacute;as dadas a la poblaci&oacute;n extranjera en Europa y de la adaptaci&oacute;n de las legislaciones nacionales a los mandatos de la Corte.</p>
        <p align="justify"><strong>Palabras  clave:</strong> Medidas de alejamiento, derechos humanos, extranjeros en Europa, vida privada y familiar, jurisprudencia.</p>&acute;
        <p align="justify"><strong>R&eacute;sum&eacute;:</strong> La r&eacute;gulation de l&acute;immigration en Europe met souvent en tension deux principes fondateurs de l&acute;Etat de droit: l&acute;exercice souverain de contrôle des fronti&egrave;res et le respect des droits de l&acute;homme. Ainsi, la Cour europ&eacute;enne des droits de l&acute;homme a d&eacute;velopp&eacute; une jurisprudence qui cherche &agrave; r&eacute;tablir l&acute;&eacute;quilibre entre le respect de la vie priv&eacute;e et familiale des &eacute;trangers et la l&eacute;gitimit&eacute; des mesures d&acute;&eacute;loignement &eacute;mises par les &eacute;tats &agrave; l&acute;encontre de ceux-ci. Cet article de r&eacute;flexion essaie de d&eacute;velopper l&acute;applicabilit&eacute; de l&acute;article 8 de la Convention europ&eacute;enne des droits de l&acute;homme: «Droit au respect de la vie priv&eacute;e et familiale», en tenant compte de la marge nationale d&acute;appr&eacute;ciation des &eacute;tats et de l&acute;analyse de la proportionnalit&eacute;, &agrave; partir de la jurisprudence de la Cour. Cette r&eacute;flexion s&acute;attachera &agrave; montrer l&acute;&eacute;volution de la jurisprudence, pour finir sur un bilan de l&acute;effectivit&eacute; de l&acute;article 8 de la Convention face au durcissement des conditions et des garanties accord&eacute;es aux &eacute;trangers et &agrave; l&acute;adaptation des l&eacute;gislations nationales en concordance avec la jurisprudence de la Cour.</p>
        <p align="justify"><strong>Mots cl&eacute;s: </strong> Mesures d&acute;&eacute;loignement, droits de l&acute;homme, &eacute;trangers en Europe, vie priv&eacute;e et familiale, jurisprudence.</p>
        <p align="justify"><strong>Abstract:</strong>The immigration enforcement in Europe produces a constant tension between the sovereign exercise of border control and the state&acute;s duty to respect human rights. In this regard, the European Court of Human Rights has developed a case law that seeks to restore the harmony between respect for private and family life of foreigners and the legitimacy of restraining orders issued by States. This reflective paper studies the application of Article 8 of the European Convention on Human Rights: &ldquo;Right to respect for private and family life&rdquo;, taking into account the so-called national margin of appreciation of States and the analysis of proportionality in the case law of the Court. Then, the analysis shows the evolution of case law in the matter, ending with a balance between the effectiveness of Article 8 of the Convention, given the tightening of the conditions and guarantees allowed to the foreign population in Europe, and the adaptation of national legislation with the decisions of the Court.</p>
        <p align="justify"><strong>Keywords:</strong>Restraining orders, human rights, foreigners in Europe, private
and family life, case law.</p>
