    <td><p align="justify" class="TIT2">Bienvenidos</p>
      <p align="justify" class="TIT2"><strong>Editorial</strong><br />
      _____________________________________________</p>
      <div align="justify">
        <p align="justify" class="CUERPO">Para la Facultad de  Jurisprudencia del Colegio   Mayor de Nuestra Señora del Rosario y la Asociación Cavelier  del   Derecho es un placer presentar ante la comunidad académica el Anuario    Colombiano de Derecho Internacional - ACDI.</p>
        <p align="justify" class="CUERPO">El Anuario busca promover,  en Colombia y en el   exterior, el conocimiento del Derecho Internacional y para  esto se   propone publicar artículos inéditos en las áreas del Derecho    Internacional Público, Derecho Internacional Privado, Derecho   Internacional de  los Derechos Humanos, Derecho Internacional   Humanitario y Derecho Comunitario  escritos por investigadores   nacionales y extranjeros. Así mismo, el Anuario  pretende abrir un   espacio a los jóvenes internacionalistas para que, de la mano  de   aquellos que gozan ya de un reconocimiento, publiquen sus   investigaciones  para así darlas a conocer a la comunidad académica   nacional e internacional.</p>
        <p align="justify" class="CUERPO">Todos los artículos  publicados son sometidos a   evaluación de pares académicos con el propósito de  garantizar la   calidad de los mismos. Al ser esta obra un Anuario de Derecho    Internacional, queremos invitar a publicar a todos los   internacionalistas  colombianos y extranjeros, que podrán presentar sus   artículos en cualquiera de  los idiomas oficiales de la   Organización   de los Estados Americanos - OEA, es decir,  castellano, inglés, francés y   portugués, por ser esta la organización regional  que regula todo lo   relativo al Derecho Internacional en el hemisferio  americano.</p>
        <p align="justify" class="CUERPO">El Anuario también  hace un reconocimiento a   aquellos internacionalistas que marcaron el Derecho  Internacional en el   país y que pusieron en alto el nombre de Colombia en  ésta área del   Derecho. En este sentido quiero recordar especialmente al Dr.  Jesús   María Yepes quien descolló como jurista en el mundo internacional, lo   que  le valió haber sido invitado por la   Academia de Derecho   Internacional de La Haya a su prestigioso curso  en tres ocasiones. En   efecto, en 1930 dictó &ldquo;La contribución de America Latina  al desarrollo   del derecho internacional público y privado&rdquo;; en 1934 &ldquo;Los  problemas   fundamentales del derecho de gentes en Am<span class="CUERPO">érica&rdquo; y en 1947, &ldquo;Los    acuerdos regionales y el derecho internacional&rdquo;. Para ilustrar la   magnitud y el  respeto que tenía el Dr. Yepes como internacionalista es   necesario recalcar que  el honor de haber dictado tres cursos en la   Academia sólo lo han tenido maestros del Derecho  Internacional como   Hans Kelsen y Roberto Ago.</span></p>
        <p align="justify" class="CUERPO"><span class="CUERPO">En la Academia también  estuvieron el Dr. José   Francisco Urrutia y el Dr. Diego Uribe Vargas. El  primero dictó, en   1928, el curso &ldquo;La</span> codificación del derecho internacional en  America&rdquo; y   el segundo presentó la conferencia inaugural del curso de Derecho    Internacional Público en 1984 titulada &ldquo;Los derechos humanos de tercera    generación&rdquo;. Además, el Dr. Uribe   Vargas es actualmente miembro del <em>Curatorium</em> de la   Academia de Derecho Internacional de La Haya.</p>
        <p align="justify" class="CUERPO">Hay un tercer grupo de  reconocidos   internacionalistas a quienes extendemos este reconocimiento por  haber   sido ellos quienes también abrieron, y siguen abriendo, el camino para    las nuevas generaciones de internacionalistas, entre ellos están el Dr.   Marco  Gerardo Monroy Cabra, el Dr. Rafael Nieto Navia, el Dr. César   Moyano (QEPD), el  Dr. Enrique Gaviria Liévano, el Padre Luis Fernando   Álvarez  Londoño, S.J. y, obviamente, el Dr. Germán Cavelier (QEPD)   fundador de la Asociación Cavelier  del Derecho, entidad que ha apoyado   incondicionalmente el presente proyecto.</p>
