          <p align="justify"><strong>Resumen:</strong> La eficacia de la lucha contra la impunidad y la efectiva protecci&oacute;n de los derechos humanos permanecen condicionadas en gran medida a la concepci&oacute;n de la competencia judicial y su ejercicio. A partir del estudio del caso Hissène Habr&eacute;, como asunto de relevancia internacional en materia penal y de derechos humanos, el presente art&iacute;culo analiza el funcionamiento de la competencia judicial en las experiencias de la justicia senegalesa y en especial de la Corte Africana de derechos Humanos y de los Pueblos, as&iacute; como las caracteristicas del sistema regional de proteccion, luego de identificar un denominador com&uacute;n seg&uacute;n el cual la soberan&iacute;a estatal y los intereses pol&iacute;ticos primar&iacute;an sobre los derechos de las v&iacute;ctimas y la justicia. Las reflexiones sobre la competencia judicial y el principio de competencia universal van m&aacute;s all&aacute; del marco jur&iacute;dico establecido por la convenci&oacute;n contra la tortura de 1984 y con ese enfoque son efectuadas ciertas consideraciones que facilitar&iacute;an la acci&oacute;n de la justicia en condiciones de igualdad y efectividad en un preciso contexto regional.</p>
