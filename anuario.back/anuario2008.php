          <td height="69" valign="top"><p><strong>El Derecho Internacional como fuente del Derecho Constitucional <br />
            </strong><em>Marco Gerardo Monroy Cabra (Colombia)</em></p>
            
            <a id="displayText4" href="javascript:toggle4();">Resumen</a><a href="anuario2008/6_Monroy.pdf" target="_blank">/ Archivo Completo </a>
        <br>&nbsp;</br>
        <div id="toggleText4" style="display: none">
            
            <p align="justify"><strong>Resumen:</strong> El presente trabajo trata de demostrar que el Derecho Internacional  ha llegado a constituirse en fuente del Derecho Constitucional. Para este  efecto, se analizan las relaciones entre el Derecho Internacional y el Derecho  interno haciendo un breve resumen de las teor&iacute;as dualista y monista y la  jurisprudencia internacional. Luego se indaga la recepci&oacute;n del Derecho  Internacional consuetudinario y Derecho Internacional convencional en los  ordenamientos internos, lo cual se estudia a la luz del Derecho comparado,  concluyendo que se incorporan de distinta manera de acuerdo con la Constituci&oacute;n  respectiva. A continuaci&oacute;n se analiza la jerarqu&iacute;a del Derecho Internacional  consuetudinario y convencional respecto a las normas nacionales, para lo cual  se hace un bosquejo del Derecho Constitucional comparado. Luego se estudia la  recepci&oacute;n, jerarqu&iacute;a y normatividad constitucional del Derecho Internacional en  derecho colombiano. Por &uacute;ltimo, se deducen conclusiones del an&aacute;lisis jur&iacute;dico  realizado.</p>
            <p align="justify">Las dos conclusiones principales son estas:  la primera, que la recepci&oacute;n y jerarqu&iacute;a del Derecho Internacional  (consuetudinario o convencional) respecto del Derecho dom&eacute;stico o Nacional se  resuelve de acuerdo con lo previsto en la Constituci&oacute;n de cada Estado. La  segunda, que un Estado no puede alegar disposiciones de su Constituci&oacute;n para  negarse a cumplir un tratado, pues si lo incumple incurre en responsabilidad  internacional.</p>
            <p align="justify"><strong>Palabras  clave:</strong> recepci&oacute;n, jerarqu&iacute;a, Constituci&oacute;n, Derecho  Internacional.</p>
            <p align="justify"><strong>Abstract:</strong> This article has the purpose to prove that the  Customary International Law and the Conventional International Law are sources  of Constitutional Law. First, it analyses the matter of the relations between  International Law and National or Domestic law according with the theories  dualism and monist and international decisions. Then, it studies the reception  and the hierarchy of International Customary and Conventional Law to Domestic  Law including Constitution. This matter has been studied according with several  Constitutions and the international doctrine. Then, it considers the  constitutional regulations about international law in the Constitution of the  Republic of Colombia.</p>
            <p align="justify">The general  conclusion is that International Law is incorporated in domestic law according  with the Constitution of each country. But every state has the duty to carry  out in good faith its obligations arising from treaties and other sources of  International Law, and it may not invoke provisions in its Constitutions or its  Laws as an excuse for failure to perform this duty. Accordingly, state practice  and decided cases have established this provision, and the same rule is  established in articles 27 and 46 of the Vienna Convention on Law of Treaties  of 1969.</p>
            <strong>Key Words:</strong> International Law, reception, hierarchy,  Constitution.<br /> 
          <p></p>
