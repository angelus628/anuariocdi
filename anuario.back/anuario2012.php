          <p align="justify"><strong>Resumen:</strong> La relaci&oacute;n existente entre los derechos humanos y el derecho omc plantea una serie de interrogantes de compleja soluci&oacute;n. Espec&iacute;ficamente, en lo que concierne al derecho a la salud, se aprecia una serie de circunstancias que podr&iacute;an desencadenar potenciales conflictos con los valores propios del sistema multilateral de comercio. En este sentido, se observa con preocupaci&oacute;n la incidencia que puede tener la protecci&oacute;n de las patentes farmac&eacute;uticas sobre el acceso a los medicamentos. Considerando que existe una estrecha relaci&oacute;n entre el acceso a los medicamentos y la plena realizaci&oacute;n del derecho a la salud, se vislumbra la necesidad de determinar el car&aacute;cter justiciable de la obligaci&oacute;n de velar por el derecho a la salud, contenida en distintos tratados de derechos humanos, conjuntamente con el an&aacute;lisis de la relaci&oacute;n existente entre ambos sistemas. La determinaci&oacute;n de estos aspectos ser&aacute; vital para establecer con mayor claridad el papel que est&aacute;n llamados a desempeñar los derechos humanos en el &aacute;mbito de la omc, as&iacute; como la posibilidad de incorporar por esta v&iacute;a una interpretaci&oacute;n m&aacute;s favorable al derecho a la salud.</p>
