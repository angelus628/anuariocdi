          <p class="presenta">El Anuario busca promover,  en Colombia y en el exterior, el   conocimiento del Derecho Internacional y para  esto se propone publicar   artículos inéditos en las áreas del Derecho  Internacional Público,   Derecho Internacional Privado, Derecho Internacional de  los Derechos   Humanos, Derecho Internacional Humanitario y Derecho Comunitario    escritos por investigadores nacionales y extranjeros. Así mismo, el   Anuario  pretende abrir un espacio a los jóvenes internacionalistas para   que, de la mano  de aquellos que gozan ya de un reconocimiento,   publiquen sus investigaciones  para así darlas a conocer a la comunidad   académica nacional e internacional.</p></td>
