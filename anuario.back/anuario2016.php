        <div id="toggleText5" style="display: none">
          <p align="justify"><strong>Resumen: </strong> En el &aacute;mbito interamericano, el control de convencionalidad impulsado por la Corte Interamericana de Derechos Humanos ha sido vinculado con el proceso de construcci&oacute;n de un <em>ius commune</em> en derechos humanos. Los derechos humanos han sido identificados como normas de car&aacute;cter constitucional. La universalidad de los derechos humanos contribuye a su determinaci&oacute;n como un aspecto del constitucionalismo global. Este trabajo tiene por objetivo examinar si el control de convencionalidad puede ser considerado una manifestaci&oacute;n del constitucionalismo global en el espec&iacute;fico &aacute;mbito interamericano. Nuestra hip&oacute;tesis es que el control de convencionalidad ha potenciado la aplicaci&oacute;n de un est&aacute;ndar m&iacute;nimo de derechos humanos y que, junto con el principio <em>pro homine</em>, ha estimulado la emergencia de un ius commune en derechos humanos.</p>
