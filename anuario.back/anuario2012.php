        <div id="toggleText5" style="display: none">
                  
                   <p align="justify"><strong>Abstract:</strong> Article 62 of the Statute of the International Court of Justice establishes that, should a State consider that it has an interest of legal nature which may be affected by the decision in the case, it may be permitted to intervene, being for the Court to decide upon the request. The Court&acute;s latest decisions regarding third-party intervention indicated that the arisen discussions, in the jurisprudence and doctrine, concerning the construction and application of this topic seemed to have disappeared. Nonetheless, the Court&acute;s recent decisions on Costa Rica&acute;s and Honduras&acute; requests for permission to intervene in the Territorial and Maritime Dispute between Nicaragua and Colombia seems to have revive said discussions. This article presents a general introduction to intervention, explaining the application the Court has given to it, as well as analyzes each of the requirements a State must met in cases brought before the Court for its settlement. As a conclusion, a commentary and analysis to the Court&acute;s most recent decisions on requests for permission to intervene is presented.</p>
                   <p align="justify"><strong>Keywords:</strong> International Court of Justice, discretionary intervention, article
62 of the Statute of the International Court of Justice, res judicata.</p>
                   <p align="justify"><strong>Resumen:</strong> El art&iacute;culo 62 del Estatuto de la Corte Internacional de Justicia establece que todo Estado que considere tener un inter&eacute;s de orden jur&iacute;dico que pueda ser afectado por la decisi&oacute;n de un litigio podr&aacute; solicitar intervenir, siendo la Corte quien decida respecto de dicha solicitud. Los &uacute;ltimos desarrollos jurisprudenciales de la Corte en materia de intervenci&oacute;n mostraban c&oacute;mo las discusiones surgidas, a escala jurisprudencial y doctrinal, alrededor de la interpretaci&oacute;n y aplicaci&oacute;n de la intervenci&oacute;n parec&iacute;an haber desaparecido. Sin embargo, las recientes decisiones de la Corte respecto de las solicitudes de intervenci&oacute;n de Costa Rica y Honduras en la Controversia Territorial y Mar&iacute;tima entre Nicaragua y Colombia han revivido de nuevo la controversia. Este art&iacute;culo presenta una introducci&oacute;n general a la instituci&oacute;n de la intervenci&oacute;n, explicando la aplicaci&oacute;n que la Corte ha dado a la misma y analiza cada uno de los requisitos que deben cumplirse para intervenir en asuntos sometidos a la Corte para su soluci&oacute;n. Como conclusi&oacute;n, se realiza un comentario y an&aacute;lisis de las decisiones de la Corte en las m&aacute;s recientes solicitudes de intervenci&oacute;n.</p>
                   <p align="justify"><strong>Palabras  clave:</strong> Corte Internacional de Justicia, intervenci&oacute;n discrecional, art&iacute;culo 62 del Estatuto de la Corte Internacional de Justicia, res judicata.</p>
