<p align="justify"><strong>Abstract:</strong> The nexus between human rights and wto law raises several questions of complex solution. Specifically, regarding the right to health, the existence of multiple circumstances can be foreseen, which may generate potential conflicts with the core values of the multilateral trade system. Within this scenario, the concern around the potential impact pharmaceutical patents may have over access to medicines has emerged. Having in mind the existence of a strong nexus between access to medicines and a plenty fulfillment of the right to health, the necessity on determining the enforcement (or justiciability) of the obligation of all states to provide for this right can be noticed, and this, while, at the same time, introducing into a deep analysis about the relationship between both systems. The results obtained will contribute to clarify the role human rights must play within the wto framework, as well as for examining the potential inclusion of a more benevolent interpretation toward the right to health.</p>
            <p align="justify"><strong>Key words:</strong> wto, dispute settlement, human rights, right to health, general
international law, industrial property.</p>
