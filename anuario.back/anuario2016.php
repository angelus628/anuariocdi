        <p align="justify"><strong>Abstract: </strong>In 1948, Brazil placed itself at the margin of the most important international tribunal of the moment by not adhering to the mandatory jurisdiction clause set out in Article 36-2 of the Statute of the International Court of Justice (ICJ). The central objective of this work is to convince the Brazilian State to file with the Secretary General of the United Nations(UN) a declaration of adherence to the mandatory jurisdiction clause of the ICJ Accordingly, the article analyzes the historical evolution of the icj, studies its role in the international system and evaluates the reasons that led Brazil to reject the contentious jurisdiction of the Court. Put another way, it analyzes the examples of countries such as France and the United States, which have also decided to reject the mandatory clause; it evaluates the arguments contrary to the position of the Court and demonstrates how it has contributed to world peace through the strengthening of international public law.</p>
